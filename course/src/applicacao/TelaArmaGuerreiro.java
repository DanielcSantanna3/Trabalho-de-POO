package applicacao;

import java.util.Scanner;

public class TelaArmaGuerreiro {
	
	public static void main(String[] args) {
	
	Scanner scanner = new Scanner(System.in);	
	
	double armaGuerreiro;
	
	System.out.println("ESCOLHA A ARMA QUE DESEJE PARA O GUERREIRO:");
	System.out.println("ESPADA - ATQ:+10 / DEF:+15 (Digite 1)");
	System.out.println("MACHADO - ATQ:+17 / DEF:+8  (Digite 2)");
	armaGuerreiro = scanner.nextInt();
	
	if (armaGuerreiro == 1) {
		System.out.println("Você escolheu a arma ESPADA!");
	}
	else {
		System.out.print("Você escolheu a arma MACHADO!");
	}
	}	
}